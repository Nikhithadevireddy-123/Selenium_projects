package arrays_practice;

//Check if String  starts with “j” and end 	with “a” . eg. “java”
public class string_example {

	public static void main(String[] args) {
		String str="java";
		if(str.startsWith("j") && str.endsWith("a"))
		{
			System.out.println("then a string starts with j and ends with a");
		}
		else {
		System.out.println("then a string does not starts with j and ends with a");
		}
		

	}

}
