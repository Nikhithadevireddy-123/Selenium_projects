package Control_statements_practice;

public class switch_case {

	public static void main(String[] args) {
		int val=1;
		switch(val)
		{
		case 1:System.out.println("tea");
		break;
		case 2:System.out.println("coffee");
		break;
		case 3:System.out.println("juice");
		break;
		default:System.out.println("invalid input enter valid input for tea or coffee or juice");
		}

	}

}
